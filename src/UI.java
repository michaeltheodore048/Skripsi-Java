    private void button1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button1ActionPerformed
        // TODO add your handling code here:
        String secretMsg = inSecret.getText();
        ArrayList<String> availableCover = new ArrayList();

        for (int i = 0; i < this.stegoCover.size(); i++) {
            String[] nameSize = this.stegoCover.get(i).split(" ");
            if (secretMsg.length() < Integer.parseInt(nameSize[1])) {
                availableCover.add(nameSize[0]);
            }
        }

        if (availableCover.size() > 0) {
            Random r = new Random();
            String randomizedFile = availableCover.get(r.nextInt(availableCover.size()));

            try {
                this.emb = new Embedder(randomizedFile);
            } catch (IOException ex) {
                Logger.getLogger(UI.class.getName()).log(Level.SEVERE, null, ex);
            }
            try {
                stegoOut.setText(this.emb.embed(secretMsg));
            } catch (IOException ex) {
                Logger.getLogger(UI.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            stegoOut.setText("Pesan rahasia melebihi kapasitas yang ada!");
        }
    }//GEN-LAST:event_button1ActionPerformed

