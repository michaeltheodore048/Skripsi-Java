    private void button3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button3ActionPerformed
        // TODO add your handling code here:
        FileWriter fw;
        BufferedWriter bw;
        boolean duplicate = false;

        if (judul.getText().equals("")) {
            labelPeringatan.setText("Isi nama file!");
        } else {

            for (int i = 0; i < this.stegoCover.size(); i++) {
                String[] nameSize = this.stegoCover.get(i).split(" ");
                if (nameSize[0].equalsIgnoreCase(judul.getText() + ".txt")) {
                    duplicate = true;
                    break;
                }
            }

            if (duplicate) {
                labelPeringatan.setText("Nama file sudah dipakai!");
                judul.setText("");
            } else {
                try {
                    // TODO add your handling code here:
                    fw = new FileWriter("stegoCover/" + judul.getText() + ".txt");
                    bw = new BufferedWriter(fw);
                    bw.write(coverIn.getText());
                    bw.close();

                    fw = new FileWriter("stegoCoverList.txt", true);
                    bw = new BufferedWriter(fw);
                    bw.newLine();
                    bw.write(judul.getText() + ".txt " + coverIn.getText().split(" ").length / 7);
                    bw.close();

                    this.stegoCover.add(judul.getText() + ".txt " + coverIn.getText().split(" ").length / 7);

                    labelPeringatan.setText("Stego cover berhasil ditambahkan");
                    judul.setText("");
                    coverIn.setText("");
                } catch (IOException ex) {
                    Logger.getLogger(UI.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
    }//GEN-LAST:event_button3ActionPerformed

    private void button4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button4ActionPerformed
        // TODO add your handling code here:
        PemotongKata pk = new PemotongKata();

        if (pk.getJumlahSukuKata(kataCover.getText()) == pk.getJumlahSukuKata(sinonim.getText())) {
            labelPeringatan1.setText("Cari sinonim lain!");
            sinonim.setText(pk.generateLevel3(pk.generateLevel2(pk.generateLevel1(sinonim.getText()))).toString());
        } else {
            try {
                FileWriter fw = new FileWriter("Tesaurus.txt", true);
                BufferedWriter bw = new BufferedWriter(fw);

                bw.newLine();
                bw.write(kataCover.getText() + " " + sinonim.getText());

                bw.close();
                fw.close();
                labelPeringatan1.setText("Sinonim berhasil ditambahkan!");
            } catch (IOException ex) {
                Logger.getLogger(UI.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_button4ActionPerformed

